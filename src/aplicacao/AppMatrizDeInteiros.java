    private Boolean validaLinhas() {
        if (txtLinhas.getText().isEmpty() || 
            Integer.parseInt(txtLinhas.getText()) <= 0) {
            JOptionPane.showMessageDialog(
                    this, 
                    "Informe um valor valido para linhas", 
                    "Manipula Matriz",
                    0);
            return true;
        } else {
            return false;
        }
    }
    
    private Boolean validaColunas() {
        if (txtColunas.getText().isEmpty() || 
            Integer.parseInt(txtColunas.getText()) <= 0) {
            JOptionPane.showMessageDialog(
                    this, 
                    "Informe um valor valido para Colunas", 
                    "Manipula Matriz", 
                    0);
            return true;
        } else {
            return false;
        }
    }
    
